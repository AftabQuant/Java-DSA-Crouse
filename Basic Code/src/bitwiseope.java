public class bitwiseope {
    public static void main(String[] args) {
        int a=9,b=10;
        System.out.println(a|b);
        System.out.println(a&b);
        System.out.println(a^b);
        System.out.println(~a);
        System.out.println(a<<1);//18
        System.out.println(a>>1);//4
    }
}
