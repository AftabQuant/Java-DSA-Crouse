public class substring{
    public static void main(String[] args) {
        String str = "abcde";
        System.out.println(str.substring(0,3));
        System.out.println(str.substring(2));
        String s = "physics";
        for(int i =2; i<4; i++){
            System.out.println(s.substring(i));
        }
    }
}
